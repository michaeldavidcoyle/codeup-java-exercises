public class StringExercise {
    public static void main(String[] args) {
        /*
         * For each of the following output examples: create a String variable that contains
         * the desired output and print it out to the console, you can do this with only one
         * String variable and one print statement for each output example.
         */

        // We don't need no education
        // We don't need no thought control
        String need = "We don't need no education\nWe don't need no thought control";
        System.out.println(need);

        // Check "this" out!, "s inside of "s!
        String checkThisOut = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkThisOut);

        // In windows, the main drive is usually C:\
        String windowsMainDrive = "In windows, the main drive is usually C:\\";
        System.out.println(windowsMainDrive);

        // I can do backslashes \, double backslashes \\, and the amazing triple backslash \\\!
        String backslashesGalore = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backslashesGalore);
    }
}
